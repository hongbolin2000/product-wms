/**
 * 框架主题
 */
export enum ThemeMode {

  /**
   * 暗黑主题
   */
  DARK = 'dark',

  /**
   * 明亮主题
   */
  LIGHT = 'light'
}

/**
 * 终端类型
 */
export enum DeviceType {

  /**
   * 电脑端
   */
  PC = 'pc',

  /**
   * 平板端
   */
  PAD = 'pad',

  /**
   * 手机端
   */
  MOBILE = 'mobile',
}

/**
 * 侧边栏主题
 */
export enum SideTheme {

  /**
   * 暗黑
   */
  DARK = 'dark',

  /**
   * 纯白
   */
  WHITE = 'white',

  /**
   * 背景图
   */
  IMAGE = 'image',
}
